            PreparedStatement stmt = conn.prepareStatement(insertQuery);
            stmt.setString(1, merkMobil);
            stmt.setString(2, ModelMobil);
            stmt.setString(3, PlatMobil);
            stmt.setString(4, hargaSewa);
            stmt.setString(5, status);
            stmt.setString(6, TahunMobil);
            stmt.executeUpdate();

