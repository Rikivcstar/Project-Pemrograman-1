    private void loadDataToForm() {
    if (selectedUserId == null) {
        return;
    }
    
    try {
        Connection conn = Config.DatabaseConnection.connect();
        String query = "SELECT merek, model, plat, harga_sewa_per_hari, status, tahun FROM mobil WHERE mobil_id = ?";
        PreparedStatement stmt = conn.prepareStatement(query);
        stmt.setString(1, selectedUserId);
        ResultSet rs = stmt.executeQuery();
        
        if (rs.next()) {
            // Isi form dengan data dari database
            merk.setText(rs.getString("merek"));
            model.setText(rs.getString("model"));
            plat.setText(rs.getString("plat"));
            harga.setText(rs.getString("harga_sewa_per_hari"));
            tahun.setText(rs.getString("tahun"));
            // Set combo box role
            String status = rs.getString("status");
            jComboBox1.setSelectedItem(status);
        }
        
        conn.close();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
    }
}
    
    private void tampilDataMobil(){
         try {
        Connection conn = Config.DatabaseConnection.connect();
        String sql = "SELECT * FROM mobil"; // hanya ambil id & username
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        // Buat model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Merk");
        model.addColumn("Model");
        model.addColumn("Plat");
        model.addColumn("Harga");
        model.addColumn("Status");
        model.addColumn("tahun");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("mobil_id"),
                rs.getString("merek"),
                rs.getString("model"),
                rs.getString("plat"),
                rs.getString("harga_sewa_per_hari"),
                rs.getString("status"),
                rs.getString("tahun")
            });
        }

        jTable1.setModel(model); // tampilkan di JTable
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }

