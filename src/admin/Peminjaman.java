    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
          int selectedRow = jTable1.getSelectedRow();

            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus.");
                return;
            }

            // Ambil ID dari baris yang dipilih
            int id = (int) jTable1.getValueAt(selectedRow, 0); // asumsi kolom ID di kolom ke-0

            int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }

            try {
                Connection conn = Config.DatabaseConnection.connect();
                String sql = "DELETE FROM peminjam WHERE id_peminjam = ?";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setInt(1, id);
                int rowsAffected = stmt.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                    tampilDataPeminjam(); // refresh tabel setelah hapus
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data.");
                }
            nama.setText("");
            nik.setText("");
            alamat.setText("");
            email.setText("");
            telepon.setText("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String namaPeminjam = nama.getText().trim();
        String nikPeminjam = nik.getText().trim();
        String Alamat = alamat.getText().trim();
        String Email = email.getText().trim();
        String Telepon = telepon.getText().trim();

        // Validasi input kosong
        if (namaPeminjam.isEmpty() || nikPeminjam.isEmpty() || Alamat.isEmpty() || 
            Email.isEmpty() || Telepon.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi.");
            return;
        }

        // Validasi apakah ada data yang dipilih untuk diedit
        if (selectedUserId == null) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diedit terlebih dahulu.");
            return;
        }

        try {
            Connection conn = Config.DatabaseConnection.connect();

            // Cek username duplikat (kecuali untuk user yang sedang diedit)
            String cekQuery = "SELECT COUNT(*) FROM peminjam WHERE nik = ? AND id_peminjam != ?";
            PreparedStatement cekStmt = conn.prepareStatement(cekQuery);
            cekStmt.setString(1, nikPeminjam);
            cekStmt.setString(2, selectedUserId);
            ResultSet rs = cekStmt.executeQuery();
            rs.next();
            if (rs.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "Nik sudah digunakan!");
                return;
            }

            // Update data
            String updateQuery;
            PreparedStatement stmt;
   
            updateQuery = "UPDATE peminjam SET nama = ?, nik = ?, alamat = ?,"
                        + "  email = ?, telepon = ? WHERE id_peminjam = ?";
            stmt = conn.prepareStatement(updateQuery);
            stmt.setString(1, namaPeminjam);
            stmt.setString(2, nikPeminjam);
            stmt.setString(3, Alamat);
            stmt.setString(4, Email);
            stmt.setString(5, Telepon);
            stmt.setString(6, selectedUserId);
           

            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate.");

            // Clear form setelah berhasil
            nama.setText("");
            nik.setText("");
            alamat.setText("");
            email.setText("");
            telepon.setText("");
            selectedUserId = null;

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengupdate: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

