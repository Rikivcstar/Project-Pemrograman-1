    private void loadDataToForm() {
    if (selectedUserId == null) {
        return;
    }
    
    try {
        Connection conn = Config.DatabaseConnection.connect();
        String query = "SELECT nama, nik, alamat, email, telepon FROM peminjam WHERE id_peminjam = ?";
        PreparedStatement stmt = conn.prepareStatement(query);
        stmt.setString(1, selectedUserId);
        ResultSet rs = stmt.executeQuery();
        
        if (rs.next()) {
            // Isi form dengan data dari database
            nama.setText(rs.getString("nama"));
            nik.setText(rs.getString("nik"));
            alamat.setText(rs.getString("alamat"));
            email.setText(rs.getString("email"));
            telepon.setText(rs.getString("telepon"));
        }
        
        conn.close();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
    }
}
    
    private void tampilDataPeminjam(){
    
      try {
        Connection conn = Config.DatabaseConnection.connect();
        String sql = "SELECT * FROM peminjam"; // hanya ambil id & username
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        // Buat model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Nama");
        model.addColumn("Nik");
        model.addColumn("Alamat");
        model.addColumn("Email");
        model.addColumn("Telepon");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_peminjam"),
                rs.getString("nama"),
                rs.getString("nik"),
                rs.getString("alamat"),
                rs.getString("email"),
                rs.getString("telepon")
               
            });
        }

        jTable1.setModel(model); // tampilkan di JTable
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }

