    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
          // TODO add your handling code here:
        String Nama = nama.getText().trim();
        String PlatMobil = nopol.getText().trim();
        String Nik = nik.getText().trim();
        String modelMobil = model.getText().trim();
        String merkMobil = merek.getText().trim();

        // Validasi input kosong
        if (Nama.isEmpty() || modelMobil.isEmpty() || PlatMobil.isEmpty() || 
            merkMobil.isEmpty() || Nik.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi.");
            return;
        }

        // Validasi apakah ada data yang dipilih untuk diedit
        if (selectedUserId == null) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diedit terlebih dahulu.");
            return;
        }

        try {
            Connection conn = Config.DatabaseConnection.connect();

            // Cek username duplikat (kecuali untuk user yang sedang diedit)
            String cekQuery = "SELECT COUNT(*) FROM pengembalian WHERE nopol = ? AND id_pengembalian != ?";
            PreparedStatement cekStmt = conn.prepareStatement(cekQuery);
            cekStmt.setString(1, PlatMobil);
            cekStmt.setString(2, selectedUserId);
            ResultSet rs = cekStmt.executeQuery();
            rs.next();
            if (rs.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "Nopol sudah digunakan!");
                return;
            }

            // Update data
            String updateQuery;
            PreparedStatement stmt;
   
            updateQuery = "UPDATE pengembalian SET nama = ?, nik = ?, nopol = ?,"
                        + "  model = ?, merk = ?  WHERE id_pengembalian = ?";
            stmt = conn.prepareStatement(updateQuery);
            stmt.setString(1, Nama);
            stmt.setString(2, Nik);
            stmt.setString(3, PlatMobil);
            stmt.setString(4, modelMobil);
            stmt.setString(5, merkMobil);
            stmt.setString(6, selectedUserId);
           

            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate.");

            // Clear form setelah berhasil
            nama.setText("");
            nik.setText("");
            nopol.setText("");
            model.setText("");
            merek.setText("");
            selectedUserId = null;

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengupdate: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

