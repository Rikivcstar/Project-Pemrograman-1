    private void loadDataToForm() {
    if (selectedUserId == null) {
        return;
    }
    
    try {
        Connection conn = Config.DatabaseConnection.connect();
        String query = "SELECT nama, nik, nopol, model, merk FROM pengembalian WHERE id_pengembalian = ?";
        PreparedStatement stmt = conn.prepareStatement(query);
        stmt.setString(1, selectedUserId);
        ResultSet rs = stmt.executeQuery();
        
        if (rs.next()) {
            // Isi form dengan data dari database
            nama.setText(rs.getString("nama"));
            nik.setText(rs.getString("nik"));
            nopol.setText(rs.getString("nopol"));
            model.setText(rs.getString("model"));
            merek.setText(rs.getString("merk"));
        }
        
        conn.close();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
    }
}
    
    private void tampilDataPengembalian(){
         try {
        Connection conn = Config.DatabaseConnection.connect();
        String sql = "SELECT * FROM pengembalian"; // hanya ambil id & username
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        // Buat model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("NAMA");
        model.addColumn("NIK");
        model.addColumn("NOPOL");
        model.addColumn("MODEL");
        model.addColumn("MEREK");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_pengembalian"),
                rs.getString("nama"),
                rs.getString("nik"),
                rs.getString("nopol"),
                rs.getString("model"),
                rs.getString("merk")
            });
        }

        jTable1.setModel(model); // tampilkan di JTable
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }

